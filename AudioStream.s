;-------------------------------------------------------------------------------

AS_CMD			equ		$420				; audio stream command register

;-------------------------------------------------------------------------------

AS_CMD_VOL		equ		%11000000			; parameters in lower 5 bits
AS_CMD_PLAY		equ		%10000000
AS_CMD_STOP		equ		%01000000
AS_CMD_PAUSE	equ		%00000011
AS_CMD_RESUME	equ		%00000010
AS_CMD_RESET	equ		%00000000

;-------------------------------------------------------------------------------
; Set volume from 0-31, pass volume in A
;-------------------------------------------------------------------------------

AudioStreamSetVolume:
			ora			#AS_CMD_VOL
			bne			.write

;-------------------------------------------------------------------------------
; Play audio track from 0-31, pass track number in A
;-------------------------------------------------------------------------------

AudioStreamPlayTrack:
			ora			#AS_CMD_PLAY
			bne			.write

;-------------------------------------------------------------------------------
; Stop audio
;-------------------------------------------------------------------------------

AudioStreamStop:
			lda			#AS_CMD_STOP
			bne			.write

;-------------------------------------------------------------------------------
; Pause audio
;-------------------------------------------------------------------------------

AudioStreamPause:
			lda			#AS_CMD_PAUSE
			bne			.write

;-------------------------------------------------------------------------------
; Resume audio
;-------------------------------------------------------------------------------

AudioStreamResume:
			lda			#AS_CMD_RESUME
			bne			.write

;-------------------------------------------------------------------------------
; Reset audio, call at least once during setup
;-------------------------------------------------------------------------------

AudioStreamReset:
			lda			#AS_CMD_RESET
.write:		sta			AS_CMD
			rts
